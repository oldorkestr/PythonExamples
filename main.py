# Введення даних
user_info = {}
user_info["ім'я"] = input("Введіть ваше ім'я: ")
user_info['вік'] = int(input("Введіть ваш вік: "))
user_info['зріст'] = float(input("Введіть ваш зріст (в см): "))
user_info['вага'] = float(input("Введіть вашу вагу (в кг): "))

# Обчислення ІМТ
height_m = user_info['зріст'] / 100
bmi = user_info['вага'] / (height_m ** 2)
user_info['ІМТ'] = bmi

# Виведення результатів
print(f"\nІм'я: {user_info["ім'я"]}")
print(f"Вік: {user_info['вік']}")
print(f"Зріст: {user_info['зріст']} см")
print(f"Вага: {user_info['вага']} кг")
print(f"Індекс маси тіла (ІМТ): {user_info['ІМТ']:.2f}")

# Перевірка на надлишкову вагу
if user_info['ІМТ'] > 25:
    print("Ви маєте надлишкову вагу.")
else:
    print("Ваш вага в нормі.")

# Введення щоденних записів
journal_entries = []
while True:
    entry = input("Введіть щоденний запис про фізичну активність або 'вихід' для завершення: ")
    if entry.lower() == 'вихід':
        break
    journal_entries.append(entry)

# Запис у файл
with open("health_journal.txt", "w") as file:
    file.write("Інформація про користувача:\n")
    for key, value in user_info.items():
        file.write(f"{key}: {value}\n")
    file.write("\nЗаписи про фізичну активність:\n")
    for entry in journal_entries:
        file.write(f"{entry}\n")
